				<h3>La Villa Savoye :</h3>

				<p>Villa Savoye is a villa located at 82, rue de Villiers, in the French town of Poissy, in Yvelines. Built from 1928 to 1931 by the architect Le Corbusier and his cousin Pierre Jeanneret. Rising on a plot of seven hectares, this construction, baptized “the Clear Hours” by its owners and described as a “living machine” by its architect, completes the period known as the architect's white villas. Characterized by its purity and harmony, this major building in the history of twentieth century architecture in the field of private individual residences, retains its avant-garde character. It consists of a white parallelepiped supported by thin piles and covered with band windows, topped with roof terraces. The site is inscribed, along with 16 other architectural works by Le Corbusier, on the UNESCO World Heritage List in 2016.</p>

				<h3>Biography of Le Corbusier :</h3>
				<p>Charles-Édouard Jeanneret-Gris is, through his father Georges-Édouard Jeanneret-Gris, the descendant of a line of Protestant Swiss craftsmen, whom he claims to have emigrated from southwestern France, but who originate from the village du Locle, near La Chaux-de-Fonds, at least until the 16th century, where his last known ascendant Jehan Jeanneret was born in 1529, fifteen generations earlier. Through his mother Marie-Charlotte-Amélie Perret, Le Corbusier descends from a line of industrial Swiss watchmakers, previously from Walloon Brabant in Belgium, later passing through Brussels. Here again, the architect wanted to relate to "origins in the north of France" and not to his true Belgian and Swiss origins. We find the surname of the Walloon "Le Corbésier" in his great-grandmother Caroline Le Corbésier (in Walloon, "corbésier" is the profession of the one who manufactures delicate Cordovan leather shoes for women and children), a name that will influence one of his various pen names used in 1920 in the writing of L'Esprit Nouveau, the only review of the purist current that he leads with Amédée Ozenfant. It seems that the architect transforms history again to bring this name back to the Indian totem of the raven or Corbu, and changes it to a more honorable "Le Corbusier".</p>
		
				<h3>History :</h3>
				<p>To achieve his ideal home, Le Corbusier ignored most of the family's requests (a large living room on the ground floor, the possibility of extension among other things) who lived in the villa from 1931 to 1940, and was on the ground floor. heart of a conflict of interest between the “machine for living” and a “device to be moved”, deliberately provocative expressions of the architect. It will be little inhabited by the family because it is not adapted to the site, the climate, or the inhabitants: very quickly appear cracks, water leaks due to defective gutters, humidity (inherent in building design); the villa cannot be heated and the rooms have poor sound insulation. In 1937, overwhelmed by the water infiltration, Mrs. Savoye declared the house uninhabitable and gave the architect formal notice to make it habitable under pain of legal proceedings.<br>

				During World War II, the villa was requisitioned by the Germans and then occupied by the Americans. After the war, a farmer was allowed to wall up the windows to store grain in the villa. In 1956, it was seriously damaged and threatened with demolition. In 1958, the city of Poissy expropriates the Savoyes; the property was then reduced by six hectares for the construction of a high school and the villa was used as a Youth and Culture Center.<br>

				In 1962, the city of Poissy ceded the house to the State. The awareness of the universal dimension of the villa led to its restoration from 1963. The classification procedure began during Le Corbusier's lifetime. The building is classified as historical monuments by decree of December 16, 1965, a few months after the death of its creator. After years of abandonment, at the instigation of the then Minister of Culture, André Malraux, restoration work was carried out and lasted until 1997. In 2015, the Center des monuments nationaux restored the lodge of the gardener and opens it to the public.</p>
