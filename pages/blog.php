        <h1>Villa Savoye submerged in a Danish fjord</h1>
              
        <p>Already vandalized in the photomontages of Belgian artist Xavier Dolory, the Villa Savoye is now drowning in the fjord of Vejle (Denmark). Or rather its replica, designed by Asmund Havsteen-Mikkelsen for the Floating Art festival which takes place opposite the Fjordenhus, the first building designed by the artist Olafur Eliasson, since June 23. Presented until September 2, this 1: 1 scale reconstruction hijacks Le Corbusier's most famous building to express a critique of our time, in the light of the modernist<div id="masque3" class="masques"> utopia that emerged at the start of the 20th century.<br><br>
              
        When it was built in Poissy (Yvelines), between 1928 and 1931, the villa of the Savoye couple embodied all the precepts of the modern movement. With its stilts, roof terrace, banded windows as well as its plan and free facades, this living machine symbolizes the "promise of a new world" according to the Danish artist. A new era which places rationality and the aspiration for a better life for all at the top of the scale of values, unlike our present, punctuated by Brexit, the election of Donald Trump or the rise of extreme parties -right…</p></div>
              
