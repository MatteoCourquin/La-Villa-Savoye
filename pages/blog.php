        <h1>A UFO named Savoye</h1>
        
        <p>You have to imagine the landscape in the 1930s. Poissy, a peaceful little town with its bell tower and stone houses. In the heights, a plateau bordered by fields and orchards stretching to the Seine. There, in the middle of this bucolic setting, a white cube, as if placed on the grass, looks like a UFO. This is the Villa Savoye.
        Imagined by Le Corbusier (see elsewhere), it will be one of the purest examples of modern architecture. So much so that the building, like all the work of its designer, was classified in July as a UNESCO World Heritage Site. A museum dedicated to this visionary is due to open there in 2020.<br><br>
        
        It was in 1928 that Eugenie and Pierre Savoye, a wealthy Parisian couple asked Charles-Edouard Jeanneret, better known <div id="masque2" class="masques">under the pseudonym of Le Corbusier, to build him a residence in the countryside. "Mrs. Savoye had spotted it while visiting the Villa Church, in Ville-d'Avray, which belonged to friends", explains David Madec, administrator of the building managed by the center of national monuments. The couple almost give him carte blanche, simply outlining the family's needs. With its stilts, its facades pierced with long windows and its garden roofs, the result detonates. Even more, it is a true manifesto of modernity that Le Corbusier imagined. The grand staircases, the chandeliers, the woodwork of classical architecture have disappeared. Thanks to the reinforced concrete, the load-bearing walls are eliminated. The light is everywhere. A solarium is fitted out. Exit the maids' rooms on the top floors, here, the staff is housed on the ground floor to come and go without disturbing the Savoye.</p></div>
        
