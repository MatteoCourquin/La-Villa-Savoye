        <h1>Exhibition in Poissy: animal and human presences haunt the Villa Savoye</h1>
        
        <p>The National Monuments Center which manages the Savoye villa in Poissy (Yvelines) is presenting the Living in the Villa exhibition, until January 24. This is a carte blanche given to Françoise Pétrovitch, artist, 56 years old and living in Cachan (94). For the occasion, she created around fifteen works, paintings and sculptures, which interact with the architecture and chromatic research of Le Corbusier.<br><br>
        
        Considered one of the flagships of the Modern Movement, the Villa Savoye takes advantage of architectural polychromy, a strategy of relating colors to spaces, designed by Le Corbusier. Using this same range of colors and adjusting the dimensions of the works to the format of the architecture, Françoise Pétrovitch<div id="masque5" class="masques"> prolongs the architect's gesture and follows in those of Eugénie Savoye, the villa's sponsor in 1928. All of the works works are part of architecture, inhabits it, even haunts it with animal and human presences.<br><br>
        
        The route begins with the vestibule on the first floor where the visitor is greeted by a bouquet painting in a vase, placed unceremoniously on the shelf. Further on, the son's bedroom receives, in the alcoves, two paintings of adolescents, in ambiguous attitudes, between idleness and worry. Around the bathroom, along the tub, the bronze sculpture of a boy surrounded by a buoy creates a vibrant contrast of materials. As an extension, the boudoir is decorated with a painting of a bird and ceramic gloves sketching games of hands in front of the window.<br><br>
        
        Continuing out onto the terrace, Jane, a black bronze sculpture, arms raised and surrounded by a belt of plants, glows darkly in the white light of the solarium. Finally, the kitchen also spares its curiosities, such as the octopus placed in ambush in the sink, the languid cat on the worktop and the plate in the serving hatch.<br><br>
        
        The artist’s familiar world (flowers, adolescents, animals) repopulates the villa. Standing right on the edge of the imagination, the works inspire a sensitive way of inhabiting.</p></div>
        
